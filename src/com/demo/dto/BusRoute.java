package com.demo.dto;

public class BusRoute {
	
	private String routeNo;
	private String stopNo;
	private String pickTime;
	private String dropTime;
	private String stopName;
	private String slabForTransFee;
	public String getRouteNo() {
		return routeNo;
	}
	public void setRouteNo(String routeNo) {
		this.routeNo = routeNo;
	}
	public String getStopNo() {
		return stopNo;
	}
	public void setStopNo(String stopNo) {
		this.stopNo = stopNo;
	}
	public String getPickTime() {
		return pickTime;
	}
	public void setPickTime(String pickTime) {
		this.pickTime = pickTime;
	}
	public String getDropTime() {
		return dropTime;
	}
	public void setDropTime(String dropTime) {
		this.dropTime = dropTime;
	}
	public String getStopName() {
		return stopName;
	}
	public void setStopName(String stopName) {
		this.stopName = stopName;
	}
	public String getSlabForTransFee() {
		return slabForTransFee;
	}
	public void setSlabForTransFee(String slabForTransFee) {
		this.slabForTransFee = slabForTransFee;
	}

	
}
